type ChessPieceProps = {
  color: Color;
  piece: PieceSymbol;
  className?: string;
};

type PieceShape =
  | { kind: "path"; layer: "keyline" | "face"; d: string }
  | { kind: "circle"; layer: "keyline" | "face"; cx: number; cy: number; r: number };

type PieceArtwork = {
  viewBox: string;
  shapes: PieceShape[];
};

export default function ChessPiece({ color, piece, className }: ChessPieceProps) {
  const artwork = ARTWORK[piece];
  return (
    <svg
      className={["chess-piece", color === "w" ? "is-white" : "is-black", className]
        .filter(Boolean)
        .join(" ")}
      viewBox={artwork.viewBox}
      aria-hidden="true"
      focusable="false"
    >
      {artwork.shapes.map((shape, index) => {
        const shapeClassName = `chess-piece__${shape.layer}`;
        if (shape.kind === "circle") {
          return (
            <circle
              key={index}
              className={shapeClassName}
              cx={shape.cx}
              cy={shape.cy}
              r={shape.r}
            />
          );
        }
        return <path key={index} className={shapeClassName} d={shape.d} />;
      })}
    </svg>
  );
}

